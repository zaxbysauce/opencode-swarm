describe('skill_regenerate tool', () => {
	// ------------------------------------------------------------------
	// Test 1: Happy path delegation
	// ------------------------------------------------------------------
	it('happy path: delegates to regenerateSkill and returns JSON result', async () => {
		const entry1 = makeEntry('tool-entry-1', {
			tags: ['scope'],
			lesson: 'tool delegation test lesson',
		});
		const entry2 = makeEntry('tool-entry-2', {
			tags: ['scope'],
			lesson: 'tool delegation test second lesson',
		});
		await seed([entry1, entry2]);

		const skillContent = buildFrontmatter('tool-test-skill', [
			'tool-entry-1',
			'tool-entry-2',
		]);
		await createActiveSkill('tool-test-skill', skillContent);

		// process.chdir(tmp) in beforeEach ensures process.cwd() returns tmp
		const result = await skill_regenerate.execute({ slug: 'tool-test-skill' });
		const parsed = JSON.parse(result);

		expect(parsed.regenerated).toBe(true);
		expect(parsed.entryCount).toBe(2);
	});

	// ------------------------------------------------------------------
	// Test 2: Missing slug returns error
	// ------------------------------------------------------------------
	it('missing slug returns error JSON with reason "slug required"', async () => {
		const result = await skill_regenerate.execute({});
		const parsed = JSON.parse(result);

		expect(parsed.regenerated).toBe(false);
		expect(parsed.reason).toBe('slug required');
	});

	it('null slug returns error JSON with reason "slug required"', async () => {
		const result = await skill_regenerate.execute({ slug: null });
		const parsed = JSON.parse(result);

		expect(parsed.regenerated).toBe(false);
		expect(parsed.reason).toBe('slug required');
	});

	it('undefined slug returns error JSON with reason "slug required"', async () => {
		const result = await skill_regenerate.execute({ slug: undefined });
		const parsed = JSON.parse(result);

		expect(parsed.regenerated).toBe(false);
		expect(parsed.reason).toBe('slug required');
	});

	it('empty string slug returns error JSON with reason "slug required"', async () => {
		const result = await skill_regenerate.execute({ slug: '' });
		const parsed = JSON.parse(result);

		expect(parsed.regenerated).toBe(false);
		expect(parsed.reason).toBe('slug required');
	});
});

