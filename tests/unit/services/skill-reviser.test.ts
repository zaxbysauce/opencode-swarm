describe('buildDeterministicRevision', () => {
	it('inserts a Revision Notes section before Source Knowledge IDs', () => {
		const violations = [makeViolation()];
		const result = _internals.buildDeterministicRevision(
			SAMPLE_SKILL,
			1,
			violations,
		);

		const revisionIdx = result.indexOf('## Revision Notes');
		const sourceIdx = result.indexOf('## Source Knowledge IDs');

		expect(revisionIdx).toBeGreaterThan(-1);
		expect(sourceIdx).toBeGreaterThan(-1);
		expect(revisionIdx).toBeLessThan(sourceIdx);
	});

	it('updates the version in frontmatter from 1 to 2', () => {
		const violations = [makeViolation()];
		const result = _internals.buildDeterministicRevision(
			SAMPLE_SKILL,
			1,
			violations,
		);

		const versionMatch = result.match(/^version:\s*(\d+)\s*$/m);
		expect(versionMatch).not.toBeNull();
		expect(versionMatch![1]).toBe('2');
	});

	it('replaces prior Revision Notes on a second revision', () => {
		const violations = [makeViolation()];
		// First revision: version 1 -> 2
		const firstRevision = _internals.buildDeterministicRevision(
			SAMPLE_SKILL,
			1,
			violations,
		);

		// Second revision: version 2 -> 3
		const secondViolation = makeViolation({
			taskId: 'task-002',
			reviewerNotes: 'Still using jest',
		});
		const secondRevision = _internals.buildDeterministicRevision(
			firstRevision,
			2,
			[secondViolation],
		);

		// Only one Revision Notes section should exist
		const revisionNotesCount = (
			secondRevision.match(/## Revision Notes/g) || []
		).length;
		expect(revisionNotesCount).toBe(1);

		// Should reference version 3 in the revision notes
		expect(secondRevision).toContain('### Version 3');
		// Should NOT contain the old version 2 revision header
		expect(secondRevision).not.toContain('### Version 2');

		// Frontmatter should say version 3
		const versionMatch = secondRevision.match(/^version:\s*(\d+)\s*$/m);
		expect(versionMatch![1]).toBe('3');
	});

	it('preserves all other sections intact', () => {
		const violations = [makeViolation()];
		const result = _internals.buildDeterministicRevision(
			SAMPLE_SKILL,
			1,
			violations,
		);

		// All standard sections must be present
		const requiredSections = [
			'## Trigger',
			'## Required Procedure',
			'## Forbidden Shortcuts',
			'## Delegation Template',
			'## Reviewer Checks',
			'## Source Knowledge IDs',
		];
		for (const section of requiredSections) {
			expect(result).toContain(section);
		}

		// Original content in those sections should remain
		expect(result).toContain('Always use bun:test');
		expect(result).toContain('Do not use jest or vitest');
		expect(result).toContain('When writing tests');
	});
});

