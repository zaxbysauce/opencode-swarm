const SAMPLE_SKILL = `---
name: test-skill
description: Test skill for testing
generated_from_knowledge:
  - k1
source_knowledge_ids:
  - k1
generated_at: 2026-06-01T00:00:00.000Z
confidence: 0.90
status: active
version: 1
skill_origin: generated
---

<!-- generated by opencode-swarm skill-generator. Do not edit by hand; edits will be preserved on regeneration only with controlled update mode. -->

# Test skill for testing

## Trigger

- When writing tests

## Required Procedure

- Always use bun:test
- Write assertions for edge cases

## Forbidden Shortcuts

- Do not use jest or vitest

## Delegation Template

When delegating a task affected by this skill, include:

\`\`\`
SKILLS: file:.opencode/skills/generated/test-skill/SKILL.md
\`\`\`

## Reviewer Checks

- Verify each required action above appears in the diff.

## Source Knowledge IDs

- k1 — Always use bun:test for testing
`;

function makeViolation(
	overrides: Partial<ViolationContext> = {},
): ViolationContext {
	return {
		taskId: 'task-001',
		agent: 'coder',
		verdict: 'violated',
		reviewerNotes: 'Used jest instead of bun:test',
		timestamp: '2026-06-10T12:00:00.000Z',
		...overrides,
	};
}

function writeSkillFile(content: string): string {
	const skillDir = path.join(
		tmp,
		'.opencode',
		'skills',
		'generated',
		'test-skill',
	);
	mkdirSync(skillDir, { recursive: true });
	const skillPath = path.join(skillDir, 'SKILL.md');
	writeFileSync(skillPath, content, 'utf-8');
	return skillPath;
}

function ensureSwarmDir(): void {
	mkdirSync(path.join(tmp, '.swarm', 'skill-changelogs'), { recursive: true });
}

