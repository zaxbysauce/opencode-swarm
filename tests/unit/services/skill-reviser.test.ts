describe('reviseSkill — deterministic (no delegate)', () => {
	it('returns { revised: false, reason: "no_violations" } when there are no violations', async () => {
		ensureSwarmDir();
		const skillPath = writeSkillFile(SAMPLE_SKILL);

		const result = await reviseSkill({
			directory: tmp,
			slug: 'test-skill',
			skillPath,
			violationContexts: [],
			currentContent: SAMPLE_SKILL,
			currentVersion: 1,
		});

		expect(result.revised).toBe(false);
		expect(result.reason).toBe('no_violations');
	});

	it('produces a deterministic revision and writes it to disk when violations are present and no delegate is provided', async () => {
		ensureSwarmDir();
		const skillPath = writeSkillFile(SAMPLE_SKILL);

		const result = await reviseSkill({
			directory: tmp,
			slug: 'test-skill',
			skillPath,
			violationContexts: [makeViolation()],
			currentContent: SAMPLE_SKILL,
			currentVersion: 1,
			now: new Date('2026-06-10T12:00:00.000Z'),
		});

		expect(result.revised).toBe(true);
		expect(result.reason).toBe('deterministic_revision');
		expect(result.quotaConsumed).toBe(false);
	});

	it('writes the revised file with version 2 in frontmatter', async () => {
		ensureSwarmDir();
		const skillPath = writeSkillFile(SAMPLE_SKILL);

		const result = await reviseSkill({
			directory: tmp,
			slug: 'test-skill',
			skillPath,
			violationContexts: [makeViolation()],
			currentContent: SAMPLE_SKILL,
			currentVersion: 1,
			now: new Date('2026-06-10T12:00:00.000Z'),
		});

		expect(result.newVersion).toBe(2);

		const written = readFileSync(skillPath, 'utf-8');
		const versionMatch = written.match(/^version:\s*(\d+)\s*$/m);
		expect(versionMatch).not.toBeNull();
		expect(versionMatch![1]).toBe('2');
	});

	it('appends a changelog entry after deterministic revision', async () => {
		ensureSwarmDir();
		const skillPath = writeSkillFile(SAMPLE_SKILL);

		await reviseSkill({
			directory: tmp,
			slug: 'test-skill',
			skillPath,
			violationContexts: [makeViolation()],
			currentContent: SAMPLE_SKILL,
			currentVersion: 1,
			now: new Date('2026-06-10T12:00:00.000Z'),
		});

		const changelogPath = path.join(
			tmp,
			'.swarm',
			'skill-changelogs',
			'test-skill.jsonl',
		);
		expect(existsSync(changelogPath)).toBe(true);

		const changelogContent = readFileSync(changelogPath, 'utf-8').trim();
		const entry = JSON.parse(changelogContent);
		expect(entry.version).toBe(2);
		expect(entry.action).toBe('revised');
		expect(entry.reason).toBe('deterministic_revision');
	});

	it('rejects deterministic revision that does not strictly improve evals before writing', async () => {
		ensureSwarmDir();
		const currentContent = `${SAMPLE_SKILL}\nlegacy sentinel requirement\n`;
		const skillPath = writeSkillFile(currentContent);
		const evalDir = path.join(tmp, '.swarm', 'skills', 'evals', 'test-skill');
		mkdirSync(evalDir, { recursive: true });
		writeFileSync(
			path.join(evalDir, 'cases.json'),
			JSON.stringify({ required_phrases: ['legacy sentinel requirement'] }),
			'utf-8',
		);

		const result = await reviseSkill({
			directory: tmp,
			slug: 'test-skill',
			skillPath,
			violationContexts: [makeViolation()],
			currentContent,
			currentVersion: 1,
		});

		expect(result.revised).toBe(false);
		expect(result.reason).toContain('validation_failed');
		expect(readFileSync(skillPath, 'utf-8')).toBe(currentContent);
		expect(
			existsSync(
				path.join(tmp, '.swarm', 'skill-changelogs', 'test-skill.jsonl'),
			),
		).toBe(false);
		expect(
			existsSync(path.join(tmp, '.swarm', 'skills', 'rejected-edits.jsonl')),
		).toBe(true);
	});

	it('preserves all required sections in the revised file', async () => {
		ensureSwarmDir();
		const skillPath = writeSkillFile(SAMPLE_SKILL);

		await reviseSkill({
			directory: tmp,
			slug: 'test-skill',
			skillPath,
			violationContexts: [makeViolation()],
			currentContent: SAMPLE_SKILL,
			currentVersion: 1,
			now: new Date('2026-06-10T12:00:00.000Z'),
		});

		const written = readFileSync(skillPath, 'utf-8');
		const requiredSections = [
			'## Trigger',
			'## Required Procedure',
			'## Forbidden Shortcuts',
			'## Delegation Template',
			'## Reviewer Checks',
			'## Source Knowledge IDs',
		];
		for (const section of requiredSections) {
			expect(written).toContain(section);
		}
	});
});

