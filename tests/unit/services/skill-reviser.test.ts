describe('getSkillVersion', () => {
	it('reads version from frontmatter and returns the correct number', async () => {
		const skillPath = writeSkillFile(SAMPLE_SKILL);
		const version = await getSkillVersion(skillPath);
		expect(version).toBe(1);
	});

	it('returns 1 when the version field is missing from frontmatter', async () => {
		const noVersionContent = SAMPLE_SKILL.replace(/^version:\s*\d+\s*$/m, '');
		const skillPath = writeSkillFile(noVersionContent);
		const version = await getSkillVersion(skillPath);
		expect(version).toBe(1);
	});

	it('returns 1 when the file does not exist', async () => {
		const version = await getSkillVersion(
			path.join(tmp, 'nonexistent', 'SKILL.md'),
		);
		expect(version).toBe(1);
	});
});

