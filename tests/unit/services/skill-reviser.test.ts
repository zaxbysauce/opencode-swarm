describe('reviseSkill — quota', () => {
	it('returns { revised: false, reason: "quota_exhausted" } when quota is fully consumed', async () => {
		ensureSwarmDir();
		const skillPath = writeSkillFile(SAMPLE_SKILL);

		const mockDelegate = mock(
			async (_system: string, _user: string) => 'irrelevant',
		);

		// Pre-exhaust quota: write a state file showing all slots used
		const quotaDir = path.join(tmp, '.swarm');
		mkdirSync(quotaDir, { recursive: true });
		const today = new Date('2026-06-10T12:00:00.000Z')
			.toISOString()
			.slice(0, 10);
		writeFileSync(
			path.join(quotaDir, 'skill-improver-quota.json'),
			JSON.stringify({
				date: today,
				calls_used: 10,
				max_calls: 10,
				window: 'utc',
			}),
			'utf-8',
		);

		const result = await reviseSkill({
			directory: tmp,
			slug: 'test-skill',
			skillPath,
			violationContexts: [makeViolation()],
			currentContent: SAMPLE_SKILL,
			currentVersion: 1,
			delegate: mockDelegate,
			now: new Date('2026-06-10T12:00:00.000Z'),
		});

		expect(result.revised).toBe(false);
		expect(result.reason).toBe('quota_exhausted');
		expect(result.quotaConsumed).toBe(false);
		// The delegate should never have been called
		expect(mockDelegate).toHaveBeenCalledTimes(0);
	});
});

